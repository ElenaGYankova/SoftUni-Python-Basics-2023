number_of_day = int(input())

if number_of_day == 1:
    print("Monday")
elif number_of_day == 2:
    print("Tuesday")
elif number_of_day == 3:
    print("Wednesday")
elif number_of_day == 4:
    print("Thursday")
elif number_of_day == 5:
    print("Friday")
elif number_of_day == 6:
    print("Saturday")
elif number_of_day == 7:
    print("Sunday")
else:
    print("Error")
